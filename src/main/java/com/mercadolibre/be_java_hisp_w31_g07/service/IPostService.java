package com.mercadolibre.be_java_hisp_w31_g07.service;

import com.mercadolibre.be_java_hisp_w31_g07.dto.request.PostDto;
import com.mercadolibre.be_java_hisp_w31_g07.dto.response.PostResponseDto;
import com.mercadolibre.be_java_hisp_w31_g07.exception.BadRequest;
import com.mercadolibre.be_java_hisp_w31_g07.exception.NotFoundException;
import com.mercadolibre.be_java_hisp_w31_g07.model.Seller;

import java.util.UUID;

public interface IPostService {
    /**
     * Allows a seller to create a new post
     * @param newPost (dto) contains all data to create a Post, except
     *                its UUID, which is generated by this service
     * @return generated UUID so the creator can find it later on
     * @throws BadRequest if newPost is null
     */
    UUID createPost(PostDto newPost);

    /**
     * Retrieves a seller by its UUID
     * @param sellerId UUID of the searched seller
     * @return Seller object that matches the UUID
     * @throws NotFoundException if the seller cannot be found
     */
    Seller findSellerById(UUID sellerId);

    PostResponseDto findPost(UUID postId);
}
